class DownloadSong {
  final String id;
  final String title;
  final String artist;
  final String album;
  final String audioUrl;
  final String artworkUrl;

  const DownloadSong({
    required this.id,
    required this.title,
    required this.artist,
    required this.album,
    required this.audioUrl,
    required this.artworkUrl,
  });
}
